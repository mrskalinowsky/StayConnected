#pragma mark - View lifecycle

- (void)viewDidLoad
{
    [super viewDidLoad];

    NSMutableArray * theContacts = [ [ [ NSMutableArray alloc ] init ] retain ];
    ABAddressBookRef addressBook = ABAddressBookCreate();
    CFArrayRef allPeople = ABAddressBookCopyArrayOfAllPeople( addressBook );
    CFIndex peopleCount = ABAddressBookGetPersonCount( addressBook );
    
    for ( int i = 0; i < peopleCount; i++) {
        ABRecordRef ref = CFArrayGetValueAtIndex( allPeople, i );
        NSString * firstName = (NSString *) ABRecordCopyValue(ref, kABPersonFirstNameProperty);
        NSString * lastName = (NSString *) ABRecordCopyValue(ref, kABPersonLastNameProperty);
        [theContacts addObject:[NSString stringWithFormat:@"%@ %@", firstName, lastName]];
    }
    
    _contactList = theContacts;
}

